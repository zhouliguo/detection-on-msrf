                # k: [nt_after] bool Â¶ÇÊûúÊòØTrueË°®Á§∫ÂΩìÂâçtarget‰∏≠ÂøÉÁÇπÊâÄÂú®ÁöÑÊ†ºÂ≠êÁöÑ‰∏äËæπÊ†ºÂ≠ê‰πüÂØπËØ•targetËøõË°åÂõûÂΩí(ÂêéÁª≠ËøõË°åËÆ°ÁÆóÊçüÂ§±)
                j, k = ((gxy % 1 < g) & (gxy > 1)).T

                ###########################
                a = (w * 0.5 -  x_off) >= 0.25
                j = a & j
                #left = ((w * 0.5 -  x_off) >= 0.25  & (j))
                for i, n in enumerate(j):
                    if w[i] * 0.5 - x_off[i] <= 0.25 and n:
                        j[i] = False
                    elif n:
                        j[i] = True   
                for i, n in enumerate(k):
                    if h[i] * 0.5 - y_off[i] <= 0.25 and n:
                        k[i] = False
                    elif n:
                        k[i] = True  
                 ###########################'''
                
                # Á≠õÈÄâ‰∏≠ÂøÉÂùêÊ†á Ë∑ùÁ¶ªÂΩìÂâç grid_cell ÁöÑÂè≥„ÄÅ‰∏ãÊñπÂÅèÁßªÂ∞è‰∫é g=0.5 ‰∏î‰∏≠ÂøÉÂùêÊ†áÂøÖÈ°ªÂ§ß‰∫é1(ÂùêÊ†á‰∏çËÉΩÂú®Ëæπ‰∏äÔºåÂê¶ÂàôÊ≠§Êó∂Â∞±Ê≤°Êúâ4‰∏™Ê†ºÂ≠ê‰∫Ü)
