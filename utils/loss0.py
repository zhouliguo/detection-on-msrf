                l, m = ((gxi % 1 < g) & (gxi > 1)).T

                ############################
                for i, n in enumerate(l):
                    if w[i] * 0.5 + x_off[i] <= 1.25 and n:
                        l[i] = False
                    elif n:
                        l[i] = True
